import math

theta = math.pi / math.sqrt(3)

print('py: ', math.pi, ', theta: ', theta)

cosv = math.degrees(math.cos(theta))
sinv = math.degrees(math.sin(theta))
tanv = math.degrees(math.tan(theta))

print('cos, sin, tan: ', cosv, sinv, tanv)

